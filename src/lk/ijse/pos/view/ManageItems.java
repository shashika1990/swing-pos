    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String code = txtCode.getText();
            String description = txtDescription.getText();
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            int qtyOnHand = Integer.parseInt(txtQty.getText());

            ItemDTO itemDTO = new ItemDTO(code, description, unitPrice, qtyOnHand);

            boolean result = ManageItemsController.saveItem(itemDTO);

            if (result) {
                JOptionPane.showMessageDialog(this, "Item has been saved successfully");
                loadAllItems();

                txtCode.setText("");
                txtDescription.setText("");
                txtUnitPrice.setText("");
                txtQty.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to save the Item");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int selectedRow = tblItems.getSelectedRow();

            if (selectedRow == -1) {
                return;
            }

            String itemCode = tblItems.getValueAt(selectedRow, 0).toString();

            boolean result = ManageItemsController.deleteItem(itemCode);

            if (result) {
                JOptionPane.showMessageDialog(this, "Item has been deleted successfully");
                loadAllItems();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the item");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String code = txtCode.getText();
            String description = txtDescription.getText();
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            int qtyOnHand = Integer.parseInt(txtQty.getText());

            ItemDTO itemDTO = new ItemDTO(code, description, unitPrice, qtyOnHand);

            if (ManageItemsController.updateItem(itemDTO)) {
                JOptionPane.showMessageDialog(rootPane, "Updated successfully");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Item couldn't be updated");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageItems.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

