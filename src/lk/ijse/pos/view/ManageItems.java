                if (tblItems.getSelectedRow() == -1) {
                    return;
                }

                String code = tblItems.getValueAt(tblItems.getSelectedRow(), 0).toString();
                String description = tblItems.getValueAt(tblItems.getSelectedRow(), 1).toString();
